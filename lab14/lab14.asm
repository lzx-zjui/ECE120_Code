;Whole Program description:
;This program will use characters that located at x5000 and x5001 to graph the ASCII character that located at x5002. 
;The grapher will use 16*8 font by displaying character at X5000 for each 0 in the instructions in the font data and character at X5001 for each 1 in the instructions in the font data.  
;For each ASCII characters, the instructions for their shapes have been already put in FONT_DATA.
;Designed By Liang Zhixiang


;Register behavior description:
;R0 is used to put the single ASCII character to the screen
;R1 is used to store the ASCII character that we want to print first and then play a role of pointer 
;R2 is used to store the data that in the current row of FONT_DATA
;R3 is the column counter that counts from 7 to 0
;R4 is the row counter that counts from 15 to 0
;R5 is used to load the adrress of FONT_DATA 


.ORIG x3000

            AND   R4,R4,#0      ; We should first initialize the row counter to 15
            ADD   R4,R,#15  
            LDI   R1,Char2      ;Then we put the ASCII value that we want to print in R1.
     	    ADD   R1,R1,R1      ;We mupltipy R1 16 times to make the backward 8 bits of R1 become the first 8 bits of R1
            ADD   R1,R1,R1      
            ADD   R1,R1,R1
            ADD   R1,R1,R1
            LEA   R5,FONT_DATA   
            ADD   R1,R1,R5      ;First get the address of FONT_DATA and add it to R1 to get the correct offset value
	    LDR   R2,R1,#0	;Now we load the content of memory location R1 to R2 which will tell program print 1 or 0 at certain place

RESET_COLUMN   AND   R3,R3,#0       
               ADD   R3,R3,#7   ;First set the column counter to 7
	       LDR R2,R1,#0     ;After processing a row, load the content of memory location R1 to R2 again

Firstbit    ADD   R2,R2,0	;manipulate R2 to set the condition code
            BRn   PRINT1

PRINT0      LDI   R0,Char0	;If the first bit of R2 is 0, we set R0 to Char0
	    OUT
            BRnzp Eject

PRINT1      LDI   R0,Char1      ;If the first bit of R2 is 1, we set R0 to Char1
	    OUT
           

Eject       ADD   R2,R2,R2	;Update the first bit of R2 
            ADD   R3,R3,#-1	;Minus R3 after each Print
            BRzp  Firstbit    	;If column counter is still >=0,we continue from Firstbit



NEXTROW     LD    R0,Newline    ;Once we reached the end of a row we print the XA to have a new line
            OUT                    

	    ADD R1,R1,#1        ;Add R1 with one to let it point to the next row
            ADD R4,R4,#-1    	;Minus row counter with 1
            BRzp  RESET_COLUMN 	;If row counter still>=0, we continue to process another row. 


DONE         HALT

Char0      .FILL x5000
Char1      .FILL x5001
Char2      .FILL x5002

Newline     .FILL xA

